#include "libft.h"
#include <stdlib.h>

void	*ft_memalloc(size_t size)
{
	void	*mem;

	if (!(mem = (void *)malloc(size * sizeof(size_t))))
		return (NULL);
	ft_bzero(mem, size);
	return (mem);
}
